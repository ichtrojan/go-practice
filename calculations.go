package main

import "fmt"

//simple calculation examples
func main() {
	fmt.Println("1 + 2 = ", 1+2)   // Addition
	fmt.Println("5 * 2 = ", 5*2)   // Multiplication
	fmt.Println("10 - 2 = ", 10-2) // Subtraction
	fmt.Println("10 / 2 = ", 10/2) // Division
	fmt.Println("10 % 2 = ", 10%2) // Remainder
}
